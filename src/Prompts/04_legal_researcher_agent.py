class LegalResearchResult:
    """نموذج نتيجة البحث القانوني"""
    
    def __init__(self, charge, charge_id):
        self.charge = charge
        self.charge_id = charge_id
        self.statutes = []
        self.legal_elements = []
        self.principles = []
        self.precedents = []
        self.penalties = {}
    
    def add_statute(self, law, article, text):
        """إضافة مادة قانونية"""
        self.statutes.append({
            "law": law,
            "article": article,
            "text": text
        })
    
    def add_legal_element(self, element):
        """إضافة ركن قانوني"""
        self.legal_elements.append(element)
    
    def add_principle(self, principle_text, source):
        """إضافة مبدأ قانوني"""
        self.principles.append({
            "principle_text": principle_text,
            "source": source
        })
    
    def add_precedent(self, court, date, reference, principle, relevance):
        """إضافة سابقة قضائية"""
        self.precedents.append({
            "court": court,
            "date": date,
            "reference": reference,
            "principle": principle,
            "relevance": relevance
        })
    
    def set_penalties(self, minimum, maximum, aggravating=None, mitigating=None):
        """تعيين العقوبات"""
        self.penalties = {
            "minimum": minimum,
            "maximum": maximum,
            "aggravating_circumstances": aggravating or [],
            "mitigating_circumstances": mitigating or []
        }
    
    def to_dict(self):
        """تحويل النتيجة إلى قاموس"""
        return {
            "charge": self.charge,
            "charge_id": self.charge_id,
            "statutes": self.statutes,
            "legal_elements": self.legal_elements,
            "principles": self.principles,
            "precedents": self.precedents,
            "penalties": self.penalties
        }


def get_legal_researcher_prompt():
    """دالة مساعدة للحصول على prompt وكيل البحث القانوني"""
    return LEGAL_RESEARCHER_AGENT_PROMPT


