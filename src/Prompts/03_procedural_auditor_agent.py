class ProceduralAuditorAgentConfig:
    """إعدادات وكيل المراجعة الإجرائية"""
    
    AGENT_NAME = "وكيل المراجعة الإجرائية"
    AGENT_ROLE = "Review procedural compliance per Egyptian Criminal Procedure Code"
    
    # أنواع الإجراءات التي يتم مراجعتها
    PROCEDURE_TYPES = {
        "ARREST": "إجراءات القبض",
        "SEARCH": "إجراءات التفتيش",
        "INTERROGATION": "إجراءات الاستجواب",
        "DETENTION": "إجراءات الحبس الاحتياطي"
    }
    
    # أنواع البطلان
    NULLITY_TYPES = {
        "ABSOLUTE": "بطلان مطلق",
        "RELATIVE": "بطلان نسبي"
    }
    
    # حالات الإجراء
    PROCEDURE_STATUS = {
        "VALID": "صحيح",
        "INVALID": "باطل"
    }
    
    @staticmethod
    def get_prompt():
        """الحصول على نص التعليمات الكامل"""
        return PROCEDURAL_AUDITOR_AGENT_PROMPT


class ProceduralAuditResult:
    """نموذج نتيجة المراجعة الإجرائية"""
    
    def __init__(self, procedure, procedure_id, date, status):
        self.procedure = procedure
        self.procedure_id = procedure_id
        self.date = date
        self.status = status
        self.nullity_type = None
        self.legal_defects = []
        self.affected_evidence = []
        self.legal_basis = []
        self.reasoning = ""
    
    def set_nullity(self, nullity_type, defects, affected_evidence, legal_basis, reasoning):
        """تعيين تفاصيل البطلان"""
        self.nullity_type = nullity_type
        self.legal_defects = defects
        self.affected_evidence = affected_evidence
        self.legal_basis = legal_basis
        self.reasoning = reasoning
    
    def to_dict(self):
        """تحويل النتيجة إلى قاموس"""
        result = {
            "procedure": self.procedure,
            "procedure_id": self.procedure_id,
            "date": self.date,
            "status": self.status
        }
        
        if self.status == "باطل":
            result.update({
                "nullity_type": self.nullity_type,
                "legal_defects": self.legal_defects,
                "affected_evidence": self.affected_evidence,
                "legal_basis": self.legal_basis,
                "reasoning": self.reasoning
            })
        
        return result


def get_procedural_auditor_prompt():
    """دالة مساعدة للحصول على prompt وكيل المراجعة الإجرائية"""
    return PROCEDURAL_AUDITOR_AGENT_PROMPT


